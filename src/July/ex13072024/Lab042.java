package July.ex13072024;

public class Lab042 {
    public static void main(String[] args) {
        // Relational operators --> returns boolean
        // >, <, >=, <=, ==, !=, --> returns true or false
        int a = 10;
        int b = 20;
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);

    }
}
