package July.ex13072024;

public class Lab041 {
    public static void main(String[] args) {
        int a = 10;
        //Compound assignment operators
        // +=, -=, *=, /=, %=
        System.out.println(a += 10); // a = a + 10
        System.out.println(a -= 10); // a = a - 10
        System.out.println(a *= 10); // a = a * 10
        System.out.println(a /= 10); // a = a / 10
        System.out.println(a %= 10); // a = a % 10

    }
}
