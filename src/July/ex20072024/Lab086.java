package July.ex20072024;

public class Lab086 {
    public static void main(String[] args) {
        // Modulus - %
        // 10 % 2 = 0
        // 11 % 2 = 1
        int num = 11;
        if (num % 2 == 0) {
            System.out.println("Even number");
        } else {
            System.out.println("Odd number");
        }

    }
}
